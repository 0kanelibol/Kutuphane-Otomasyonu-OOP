from abc import ABC, abstractmethod

class Kaynak(ABC):
    def __init__(self, baslik, kayitNo):
       # Self bizm değişkenimizi temsil eder.
       # Burası setter'ı tetikler.
        self.baslik = baslik
        self.kayitNo = kayitNo

    # Metodumuzu değişken gibi gösterir.[baslik() = baslik]
    @property
    def baslik(self):
        return self._baslik
    # Medonumuza veri gelmeden önce kontrolünü yapar çünkü metodumuz değişken gibi tanımlanabiliyor.
    @baslik.setter
    def baslik(self, baslik):
        self._baslik = baslik

    @property
    def kayitNo(self):
        return self._kayitNo

    @kayitNo.setter
    def kayitNo(self, kayitNo):
        self._kayitNo = kayitNo

#__________________________________________________________________________________________________________________________________

class Kitap(Kaynak):
    def __init__(self, baslik, kayitNo, yazar, sayfa_sayisi):

      # super().__init__() kullanarak üst sınıfın (Kaynak) özelliklerini alıyoruz.
        super().__init__(baslik, kayitNo)

      # Kitaba özel özellikler.
        self.yazar = yazar
        self.sayfa_sayisi = sayfa_sayisi


    @property
    def yazar(self):
        return self._yazar

    @yazar.setter
    def yazar(self, yazar):
        self._yazar = yazar

    @property
    def sayfa_sayisi(self):
        return self._sayfa_sayisi

    @sayfa_sayisi.setter
    def sayfa_sayisi(self, sayfa_sayisi):
        self._sayfa_sayisi = sayfa_sayisi

#__________________________________________________________________________________________________________________________________

class Dergi(Kaynak):
    def __init__(self, baslik, kayitNo, yayin_donemi, sayi_no):

      # super() ile üst sınıfın özelliklerini çektik.
        super().__init__(baslik, kayitNo)

        # Dergiye özel özellikler
        self.yayin_donemi = yayin_donemi
        self.sayi_no = sayi_no

    @property
    def yayin_donemi(self):
        return self._yayin_donemi

    @yayin_donemi.setter
    def yayin_donemi(self, yayin_donemi):
        self._yayin_donemi = yayin_donemi

    @property
    def sayi_no(self):
        return self._sayi_no

    @sayi_no.setter
    def sayi_no(self, sayi_no):
        self._sayi_no = sayi_no

