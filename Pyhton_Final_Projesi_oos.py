# Ekle, sil, güncelle, listele metodlarının şablonu. (abstractmethod)
class Islem(ABC):

    # Kendisinden miras alınacak olan özellikler var mı kontrolünü yapar. (abstractmethod)
    @abstractmethod
    def ekle(self):
        pass

    @abstractmethod
    def sil(self):
        pass

    @abstractmethod
    def guncelle(self):
        pass

    @abstractmethod
    def listele(self):
        pass

#__________________________________________________________________________________________________________________________________

class KitapIslem(Islem):
    def __init__(self):

        # Kitapları RAM'de tutacağımız listemiz.
        self.kitap_listesi = []

    def ekle(self):

        # print() menü daha düzenli gözükmesi için bolşuk koyar.
        print()
        print("--- Kitap Ekle ---")
        kayitNo = input("Kitabın kayıt numarasını girin: ")

        # Kayıt no kontrol sistemi.
        # Class içerisine adam yolluyoruz (k) bizim için kayıtNo kısmına bakıyor bu nedenle "k.kayitNo yazarak veriye ulaşıyoruz.
        # Fakat class icerisinde self.kayıtNo yazarak ulaşabiliriz zaten içerideyiz.
        for k in self.kitap_listesi:
            if k.kayitNo == kayitNo:
                print("HATA: Bu kayıt numarası zaten sistemde var!")
                return

        # Veri girişi.
        baslik = input("Kitabın başlığını girin: ")
        yazar = input("Kitabın yazarını girin: ")
        sayfa = input("Kitabın sayfa sayısını girin: ")

        yeni_kitap = Kitap(baslik, kayitNo, yazar, sayfa)

        # append kullanarak listemize veri ekliyoruz.
        self.kitap_listesi.append(yeni_kitap)
        print("Kitap başarıyla eklendi.")

    def sil(self):
        print()
        print("--- Kitap Sil ---")
        kayitNo = input("Silinecek kitabın kayıt numarasını girin: ")
        for silinecekKitap in self.kitap_listesi:
            if silinecekKitap.kayitNo == kayitNo:

                # remove kullanarak veriyi listeden siliyoruz.
                self.kitap_listesi.remove(silinecekKitap)
                print("Kitap başarıyla silindi.")
                return
        print("Kayıt bulunamadı.")

    def guncelle(self):
        print()
        print("--- Kitap Güncelle ---")
        kayitNo = input("Güncellenecek kitabın kayıt numarasını girin: ")
        for kitap in self.kitap_listesi:
            if kitap.kayitNo == kayitNo:
                print("Yeni bilgileri girin (Değiştirmek istemiyorsanız boş bırakıp Enter'a basın):")
                yeni_baslik = input(f"Yeni Başlık ({kitap.baslik}): ")
                if yeni_baslik: kitap.baslik = yeni_baslik

                yeni_yazar = input(f"Yeni Yazar ({kitap.yazar}): ")
                if yeni_yazar: kitap.yazar = yeni_yazar

                yeni_sayfa = input(f"Yeni Sayfa Sayısı ({kitap.sayfa_sayisi}): ")
                if yeni_sayfa: kitap.sayfa_sayisi = yeni_sayfa

                print("Kitap başarıyla güncellendi.")
                return
        print("Kayıt bulunamadı.")

    def listele(self):
        print()
        print("--- Kitap Listesi ---")

        # Liste boşken uyarı verme sistemi.
        if not self.kitap_listesi:
            print("Kayıt bulunamadı.")
            return

        for kitap in self.kitap_listesi:
            print(f"Kayıt No: {kitap.kayitNo} | Başlık: {kitap.baslik} | Yazar: {kitap.yazar} | Sayfa: {kitap.sayfa_sayisi}")

